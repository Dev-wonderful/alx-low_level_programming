/**
 * add - adds two numbers
 * @a: first number
 * @b: second number
 *
 * return: integer
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two numbers
 * @a: first number
 * @b: second number
 *
 * return: integer
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two numbers
 * @a: first number
 * @b: second number
 *
 * return: integer
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two numbers
 * @a: first number
 * @b: second number
 *
 * return: integer
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulus two numbers
 * @a: first number
 * @b: second number
 *
 * return: integer
 */
int mod(int a, int b)
{
	return (a % b);
}
